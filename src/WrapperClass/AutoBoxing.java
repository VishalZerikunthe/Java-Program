package WrapperClass;

public class AutoBoxing {
    public static void main(String[] args) {
        int a=10;
        Integer obj=a;
        System.out.println(a);
        System.out.println(obj);

        boolean flag=false;
        Boolean obj1=flag;
        System.out.println(flag);
        System.out.println(obj1);
    }
}
